class Api:
    """JS↔Python bridge. JS calls these via `window.pywebview.api.*`."""

    def __init__(self):
        self.window: Optional[webview.Window] = None
        self.output_dir: Optional[str] = None
        self.operation: str = "rename"
        self._dataset: dict = {}

    # ----------------------------- helpers -----------------------------

    def _log(self, msg: str, cls: str = ""):
        """Push a line to the form's log area (if the form is showing)."""
        if not self.window:
            return
        safe = json.dumps(msg)
        cls_safe = json.dumps(cls)
        try:
            self.window.evaluate_js(f"window.zvr_log && window.zvr_log({safe}, {cls_safe});")
        except Exception:
            pass

    # ----------------------------- exposed -----------------------------

    def default_output_dir(self) -> str:
        return os.path.join(os.path.expanduser("~"), "Desktop", "video-rename-review")

    def pick_folder(self) -> Optional[str]:
        result = self.window.create_file_dialog(webview.FOLDER_DIALOG)
        if isinstance(result, (list, tuple)) and result:
            return result[0]
        return None

    def open_review(self) -> None:
        if not self.output_dir:
            return
        html_path = os.path.join(self.output_dir, "index.html")
        if os.path.exists(html_path):
            self.window.load_url("file://" + html_path)

    def go_home(self) -> None:
        """Called from the review UI to navigate back to the form."""
        self.window.load_html(FORM_HTML)

    def scan(self, stills_dir: str, videos_dir: str, out_dir: str,
             videos_only: bool, provider: str, api_key: str) -> dict:
        """Run scan synchronously (blocks JS promise until done).

        Returns {ok: bool, error?: str}. The form's JS calls open_review()
        on success to navigate to the review HTML in the same window.
        """
        try:
            self.output_dir = os.path.abspath(out_dir)
            self.operation = "rename"  # Set later via API call if user changes dropdown
            os.makedirs(os.path.join(self.output_dir, "thumbs", "stills"), exist_ok=True)
            os.makedirs(os.path.join(self.output_dir, "thumbs", "videos"), exist_ok=True)

            if videos_only:
                self._log("Videos-only mode (catalog).")
                stills_index = {}
                videos = scan_videos(videos_dir, recursive=True)
                self._log(f"Found {len(videos)} videos.")
                matches = scan_videos_only(videos)
                unmatched = []
            else:
                self._log("Scanning stills…")
                stills_index = scan_stills(stills_dir, recursive=True)
                self._log(f"Found {sum(len(v) for v in stills_index.values())} stills across {len(stills_index)} stubs.")
                self._log("Scanning videos…")
                videos = scan_videos(videos_dir, recursive=True)
                self._log(f"Found {len(videos)} videos.")
                self._log("Matching…")
                matches, unmatched = match_videos_to_stills(stills_index, videos)
                self._log(f"Matched {len(matches)} stubs; {len(unmatched)} unmatched.")

            def picker(stills):
                return pick_canonical_still(stills)

            dataset = ui.matches_to_ui_dataset(
                matches, stills_root=(stills_dir or videos_dir), videos_root=videos_dir,
                project_root=self.output_dir, canonical_picker=picker,
                suggested_names={},
            )
            dataset["mode"] = "videos-only" if videos_only else "stills+videos"
            if not videos_only:
                for entry in dataset["entries"]:
                    if entry.get("canonical_still_rel"):
                        stem = os.path.splitext(os.path.basename(entry["canonical_still_rel"]))[0]
                        cleaned = naming.name_from_still_filename(stem + ".png")
                        if naming.looks_like_descriptive_name(cleaned):
                            entry["suggested_name"] = cleaned

            if videos_only:
                self._log("Extracting 3 frames per video…")
                three_dir = os.path.join(self.output_dir, "thumbs", "video_frames")
                os.makedirs(three_dir, exist_ok=True)
                ok = 0
                for e in dataset["entries"]:
                    vid = e["videos"][0]
                    src = next((v.abs_path for v in videos if v.filename == vid["filename"]), None)
                    if not src:
                        continue
                    frames = extract_three_frames(src, three_dir, e["id"])
                    if frames.get("mid"):
                        e["still_thumb"] = os.path.relpath(frames["mid"], self.output_dir)
                        e["canonical_still_rel"] = e["still_thumb"]
                        ok += 1
                    e["_video_frames"] = {k: os.path.relpath(p, self.output_dir) for k, p in frames.items()}
                self._log(f"3-frame extraction OK for {ok}/{len(dataset['entries'])}.")

            # Save matches.json
            matches_path = os.path.join(self.output_dir, "matches.json")
            with open(matches_path, "w") as f:
                json.dump(dataset, f, indent=2, default=str)

            # Still + video thumbnails
            if not videos_only:
                self._log("Generating still thumbnails…")
                still_tasks = []
                for e in dataset["entries"]:
                    if not e["canonical_still_rel"]:
                        continue
                    src = next((s.abs_path for s in stills_index.get(e["stub"], [])
                                if os.path.relpath(s.abs_path, self.output_dir) == e["canonical_still_rel"]), None)
                    if not src and stills_index.get(e["stub"]):
                        src = stills_index[e["stub"]][0].abs_path
                    if src:
                        still_tasks.append((src, os.path.join(self.output_dir, e["still_thumb"])))
                ok_n, fail_n = batch_make_still_thumbs(still_tasks, max_workers=4)
                self._log(f"Stills: {ok_n} ok, {fail_n} failed.")

            self._log("Extracting video frame previews…")
            vid_tasks = []
            for e in dataset["entries"]:
                for v in e["videos"]:
                    src = next((vid.abs_path for vid in videos if vid.filename == v["filename"]), None)
                    if src:
                        vid_tasks.append((src, os.path.join(self.output_dir, v["thumb"]), 0.5))
            ok_n, fail_n = batch_extract_video_frames(vid_tasks, max_workers=4)
            self._log(f"Video previews: {ok_n} ok, {fail_n} failed.")

            # AI naming (optional)
            if provider and api_key:
                self._log(f"Calling {provider} for descriptive names…")
                try:
                    from .ai import get_client
                    from .ai.base import VIDEO_NAMING_PROMPT
                    client = get_client(provider, api_key=api_key)
                    raw_names: dict = {}
                    if videos_only:
                        items_multi = []
                        for e in dataset["entries"]:
                            if e.get("_video_frames"):
                                paths = [os.path.join(self.output_dir, p) for p in e["_video_frames"].values() if p]
                                paths = [p for p in paths if os.path.exists(p)]
                                if paths:
                                    items_multi.append((e["id"], paths))
                        if items_multi:
                            raw_names = naming.ai_name_batch_multi(
                                client, items_multi, prompt=VIDEO_NAMING_PROMPT, max_workers=3)
                    else:
                        items = [(e["id"], os.path.join(self.output_dir, e["still_thumb"]))
                                 for e in dataset["entries"]
                                 if e["still_thumb"] and os.path.exists(os.path.join(self.output_dir, e["still_thumb"]))]
                        raw_names = naming.ai_name_batch(client, items, max_workers=3)
                    final = naming.disambiguate_names(raw_names)
                    for e in dataset["entries"]:
                        if e["id"] in final:
                            e["suggested_name"] = final[e["id"]]
                    self._log(f"AI proposed {len(final)} names.")
                except Exception as e:
                    self._log(f"AI naming failed: {e}", "err")

            # Re-save matches.json with AI names + video_frames
            with open(matches_path, "w") as f:
                json.dump(dataset, f, indent=2, default=str)

            # Generate the review HTML
            ui.write_review_html(dataset, os.path.join(self.output_dir, "index.html"))

            self._dataset = dataset
            return {"ok": True, "entries": len(dataset["entries"])}
        except Exception as e:
            self._log(f"✗ Scan failed: {e}", "err")
            import traceback
            self._log(traceback.format_exc()[:1000], "err")
            return {"ok": False, "error": str(e)}

    def apply_renames(self, approvals: dict, operation: str = "rename") -> dict:
        """Apply renames given the approvals dict (sent from the review UI's
        Approve & Rename button via the JS bridge).

        Returns {ok, succeeded, failed, undo_log}.
        """
        try:
            if not self.output_dir:
                return {"ok": False, "error": "No project folder."}

            # Resolve project-rel paths to absolute (same as cli.cmd_apply)
            by_rel = {}
            with open(os.path.join(self.output_dir, "matches.json")) as f:
                ds = json.load(f)
            for e in ds["entries"]:
                for v in e["videos"]:
                    by_rel[v["rel_path"]] = v["abs_path"]
                for s in e["all_still_files"]:
                    by_rel[s["rel_path"]] = s["abs_path"]

            for entry in approvals.get("approved", []):
                for r in entry.get("renames", []):
                    if r.get("skip"):
                        continue
                    abs_src = by_rel.get(r["from"])
                    if abs_src:
                        r["from"] = abs_src
                    src_path = r["from"]
                    if os.path.isabs(src_path):
                        new_filename = os.path.basename(r["to"])
                        r["to"] = os.path.join(os.path.dirname(src_path), new_filename)

            plan = apply_mod.build_plan(approvals, project_root=self.output_dir)
            if plan.collisions:
                return {"ok": False, "error": f"{len(plan.collisions)} target-path collisions — fix names first."}
            if plan.missing_sources:
                return {"ok": False, "error": f"{len(plan.missing_sources)} source files missing."}

            if operation not in ("rename", "copy", "move"):
                operation = "rename"

            ok, failed, undo_path = apply_mod.execute_plan(plan, undo_log_dir=self.output_dir, operation=operation)
            return {"ok": True, "succeeded": ok, "failed": failed, "undo_log": undo_path,
                    "operation": operation}
        except Exception as e:
            import traceback
            return {"ok": False, "error": str(e), "traceback": traceback.format_exc()[:1500]}


