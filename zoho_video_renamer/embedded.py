"""Pywebview-based desktop app — everything in one native window.

This replaces the tkinter GUI (zoho_video_renamer.gui). The whole flow —
folder pickers, API key entry, scan progress, review of matched videos,
approve/exclude, final rename — happens inside a single WKWebView (macOS)
/ WebView2 (Windows) / GTK (Linux) window. No external browser, no file
export step, no Terminal.

Architecture:
- `webview.create_window(...)` opens a native window pointing at FORM_HTML
- `Api` class is exposed as `window.pywebview.api.*` to the JavaScript
- The form's Start button calls `api.scan(...)` which runs in a worker
  thread and posts progress/completion back via `evaluate_js`
- When scan completes, the window is navigated (via `load_html`) to the
  review HTML (generated by `ui.write_review_html`), which detects
  `window.pywebview` and replaces its Export-JSON button with an in-place
  Approve-and-Rename button that calls back into `api.apply_renames(...)`
"""
